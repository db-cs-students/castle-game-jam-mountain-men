// # Jumping/Gravity 
let can_double_jump = true
controller.A.onEvent(ControllerButtonEvent.Pressed, function jump() {
    
    if (can_double_jump) {
        mountianman1.vy = -100
        can_double_jump = mountianman1.isHittingTile(CollisionDirection.Bottom)
    }
    
})
controller.moveSprite(mountianman1, 50, 0)
function on_update() {
    
    if (mountianman1.isHittingTile(CollisionDirection.Bottom)) {
        can_double_jump = true
    }
    
}

